package com.supermarket.simulation.core;

/**
 * Enumerates the types of events that can occur in the supermarket simulation.
 * Each event type corresponds to a specific action in the simulation.
 */

public enum EventType {

    /**
     * An arrival event occurs when a customer arrives at the supermarket.
     */
    ARRIVAL,

    /**
     * A departure event occurs when a customer leaves the supermarket.
     */
    DEPARTURE
}
